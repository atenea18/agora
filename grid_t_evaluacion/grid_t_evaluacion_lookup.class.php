   function lookup_asig1(&$conteudo , $id_estudiante, $par_id_asig1, &$nm_array_retorno_lookup) 
   {
      $nm_array_retorno_lookup = array();
      if (trim($id_estudiante) === "")
      { 
          $conteudo = "&nbsp;";
